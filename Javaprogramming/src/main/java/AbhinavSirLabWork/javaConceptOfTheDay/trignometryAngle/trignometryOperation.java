package AbhinavSirLabWork.javaConceptOfTheDay.trignometryAngle;

public class trignometryOperation {
    public static void main(String[] args) {
        double angle=90;
        double angleInRadians=Math.toRadians(angle);
        double signOfNum=Math.sin(angleInRadians);
        System.out.println(signOfNum);
        double cosineOfNum=Math.cos(angleInRadians);
        System.out.println(cosineOfNum);
        double tanOfNum=Math.tan(angleInRadians);
        System.out.println(tanOfNum);
    }
}
